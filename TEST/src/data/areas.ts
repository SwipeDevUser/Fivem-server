// ============================================================================
// HELPER FUNCTIONS
// ============================================================================

export function getAreasByRegion(region: FloridaRegion): Area[] {
  return allAreas.filter((area) => area.region === region)
}

export function getAreaById(id: string): Area | undefined {
  return allAreas.find((area) => area.id === id)
}

export function getNearestArea(coordinates: Coordinates, maxDistance = 500): Area | null {
  return (
    allAreas.reduce(
      (nearest, area) => {
        const distance = Math.sqrt(
          Math.pow(area.coordinates.x - coordinates.x, 2) +
            Math.pow(area.coordinates.y - coordinates.y, 2)
        )
        if (distance < maxDistance && (!nearest || distance < nearest.distance)) {
          return { area, distance }
        }
        return nearest
      },
      null as { area: Area; distance: number } | null
    )?.area || null
  )
}

export function searchAreas(query: string): Area[] {
  const lowerQuery = query.toLowerCase()
  return allAreas.filter(
    (area) =>
      area.name.toLowerCase().includes(lowerQuery) ||
      area.description.toLowerCase().includes(lowerQuery) ||
      area.region.toLowerCase().includes(lowerQuery)
  )
}
